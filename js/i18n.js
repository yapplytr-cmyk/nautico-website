(function() {
  // Check for saved language preference
  var savedLang = localStorage.getItem('nautico-lang');
  var initialLang = savedLang || 'en';

  // Check browser language as fallback
  if (!savedLang && window.navigator.language) {
    var browserLang = window.navigator.language.substring(0, 2).toLowerCase();
    if (I18N[browserLang]) {
      initialLang = browserLang;
    }
  }

  // Apply language when DOM is ready
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', function() {
      setLanguage(initialLang);
    });
  } else {
    // DOM is already ready
    setLanguage(initialLang);
  }
})();
