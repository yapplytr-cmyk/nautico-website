var currentLang = 'en';

/**
 * Set the current language and apply translations to all elements with data-i18n attributes
 * @param {string} lang - Language code (en, ru, tr, fr, it, es)
 */
function setLanguage(lang) {
  // Validate language
  if (!I18N[lang]) {
    console.warn('Language not found: ' + lang + ', defaulting to en');
    lang = 'en';
  }

  currentLang = lang;

  // Apply translations to all elements with data-i18n attribute
  var elements = document.querySelectorAll('[data-i18n]');
  elements.forEach(function(el) {
    var key = el.getAttribute('data-i18n');
    if (I18N[lang][key]) {
      el.textContent = I18N[lang][key];
    }
  });

  // Handle placeholders for input fields
  var placeholders = document.querySelectorAll('[data-i18n-placeholder]');
  placeholders.forEach(function(el) {
    var key = el.getAttribute('data-i18n-placeholder');
    if (I18N[lang][key]) {
      el.setAttribute('placeholder', I18N[lang][key]);
    }
  });

  // Handle HTML content (for elements that need HTML)
  var htmlElements = document.querySelectorAll('[data-i18n-html]');
  htmlElements.forEach(function(el) {
    var key = el.getAttribute('data-i18n-html');
    if (I18N[lang][key]) {
      el.innerHTML = I18N[lang][key];
    }
  });

  // Update document language attribute
  document.documentElement.lang = lang;

  // Save language preference to localStorage
  localStorage.setItem('nautico-lang', lang);

  // Update active language indicator if it exists
  var langIndicators = document.querySelectorAll('[data-lang-indicator]');
  langIndicators.forEach(function(el) {
    el.classList.remove('active');
    if (el.getAttribute('data-lang') === lang) {
      el.classList.add('active');
    }
  });

  // Dispatch custom event so other scripts can react to language change
  var event = new CustomEvent('languageChanged', { detail: { lang: lang } });
  document.dispatchEvent(event);
}

/**
 * Get the current language
 * @returns {string} Current language code
 */
function getCurrentLanguage() {
  return currentLang;
}

/**
 * Get a translation string
 * @param {string} key - Translation key
 * @returns {string} Translated string or key if not found
 */
function t(key) {
  return I18N[currentLang][key] || key;
}

